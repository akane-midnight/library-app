package ru.vsu.va;

public class BookExemplar {
    private String bookExemplarId;
    private String bookDescription;
    private String bookId;

    public String getBookExemplarId() { return bookExemplarId; }

    public void setBookExemplarId(final String bookExemplarId) { this.bookExemplarId = bookExemplarId; }

    public String getBookDescription() { return bookDescription; }

    public void setBookDescription(final String bookDescription) { this.bookDescription = bookDescription; }

    public String getBookId() { return bookId; }

    public void setBookId(final String bookId) { this.bookId = bookId; }
}
